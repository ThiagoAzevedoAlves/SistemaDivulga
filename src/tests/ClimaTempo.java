    public ClimaTempo() {
        InputStream certidao = null;
        InputStream senha = null;
        try {
            initComponents();
            RSSFeedParser parser = new RSSFeedParser("http://servicos.cptec.inpe.br/RSS/cidade/3914/previsao.xml");
            Feed feed = parser.readFeed();
            String previsao = "";
            
            for (FeedMessage message : feed.getMessages()) {
                previsao = previsao + "\n" +message.getDescription()+ "\n";
            }   String imagem = "<html>";
            
            String [] aux = previsao.split(">", 2); //String auxiliar 0 = imagem, 1 = resto;
            
            if(aux != null){
                imagem = imagem+aux[0];
            }   imagem = imagem+"</html>";
            
            jLabel1.setText(imagem);
            String [] aux2 = aux[1].split("P");
            String [] desc = aux2[0].split("-");
            jLabel2.setText(desc[0]);
            jLabel3.setText(desc[1]);
            this.getContentPane().setBackground(Color.white);
            
            boolean found = new NativeDiscovery().discover();
            
            EmbeddedMediaPlayerComponent mediaPlayerComponent = new EmbeddedMediaPlayerComponent();
            mediaPlayerComponent.setBounds(0, 0, 800,600);
            this.jInternalFrame1.getContentPane().add(mediaPlayerComponent);
            
            //mediaPlayerComponent.getMediaPlayer().playMedia("Mezzari.mpe");
            //mediaPlayerComponent.getMediaPlayer().play();
            
            Audio audio = Audio.getInstance();
            certidao = audio.getAudio("Registros", Language.PORTUGUESE);
            senha = audio.getAudio("Senha, 5", Language.PORTUGUESE);
            audio.play(certidao);
            audio.play(senha);
        
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, ex);
            this.dispose();
        } catch (JavaLayerException ex) {
            JOptionPane.showMessageDialog(null, ex);
            this.dispose();
        } finally {
            try {
                certidao.close();
                senha.close();
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(null, ex);
                this.dispose();
            }
        }
        
    }

